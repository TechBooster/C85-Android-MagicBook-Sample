package jp.mydns.sys1yagi.android.picassosample.photo;

public class PhotoRow {

    private Photo mLeft;
    private Photo mRight;

    public PhotoRow() {

    }

    public Photo getLeft() {
        return mLeft;
    }

    public void setLeft(Photo left) {
        mLeft = left;
    }

    public Photo getRight() {
        return mRight;
    }

    public void setRight(Photo right) {
        mRight = right;
    }

}
